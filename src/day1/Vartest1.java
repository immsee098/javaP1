class Vartest1
{
	public static void main(String[] args)
	{
		/*
			변수 - 데이터를 저장하기 위한 메모리 공간을 할당하고 이름을 부여한 것

			자료형(데이터타입, Data Type) - 변수의 종류와 크기를 정하는 것

			변수 선언
			자료형 변수명;
			예) int num;
		*/
		
		//변수 선언
		int a;

		//값 할당
		a=20;

		//변수 선언과 값 할당을 동시에
		int b =25;

		//여러개 변수 선언후 할당
		int c, d, e;
		c=30;
		d=40;
		e=50;

		//여러개 변수 선언과 할당을 동시에
		int  f = 11, g = 13;

		//변수끼리의 할당
		int n = 100, m;
		m = n;

		System.out.println("a="+a+", b="+b);
		System.out.println("c="+c+", d="+d+",e="+e);
		System.out.println("f="+f+", g="+g);
		System.out.println("n="+n+", m="+m);

		//int b; ~>이미 선언한 변수를 또 선언하면 에러난다.
		//b = 300; ~> 이미 할당된 변수에 다른 값을 할당하는 것은 가능하다

	}
}